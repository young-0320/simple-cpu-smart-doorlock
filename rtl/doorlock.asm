INIT:
	LOADI 0
	STORE 230 ; 입력 버퍼 
	STORE 231 ; PMOD 입력
	STORE 232 ; 실패 횟수
	STORE 233 ; 현재 모드
	STORE 234 ; UNLOCK_FLAG = 0

	OUT 0 ; LED OFF

	LOADI 1
	STORE 242

MAIN_WAIT:
	IN 2 ; 확정버튼
	CMPI 1
	JZ COMPLETE_BTN

	IN 3 ; 취소버튼
	CMPI 1
	JZ BACKSPACE

	IN 1 ; 입력 버튼
	CMPI 1
	JZ READ_NUMBER
	
	IN 4 ; 비밀번호 변경
	CMPI 1;
	JZ CHECK_CAN_CHANGE

	JMP MAIN_WAIT

CHECK_CAN_CHANGE:
	LOAD 234 ; UNLOCK_FLAG 확인
	CMPI 1
	JZ ENTER_CHANGE_MODE
	JMP MAIN_WAIT 


ENTER_CHANGE_MODE:
	LOADI 0
	STORE 230	
	STORE 231	
	STORE 210
	STORE 243 ; 변경 비밀번호 재확인 위한 메모리
	STORE 244 ; 재입력 FLAG
	JMP CHANGE_WAIT

CHANGE_WAIT:
	IN 1 ; 입력버튼
	CMPI 1
	JZ CHANGE_READ_NUM

	IN 2 ; 확정버튼
	CMPI 1
	JZ ONE_MORE_CHECK1

	IN 3 ; 취소버튼
	CMPI 1
	JZ CHANGE_BACKSPACE

	JMP CHANGE_WAIT

CHANGE_BACKSPACE:
	LOAD 210
	CMPI 0
	JZ CHANGE_WAIT

	LOAD 230
	SHR 4
	STORE 230

	LOAD 210
	SUB 242
	STORE 210

	JMP CHANGE_WAIT

CHANGE_READ_NUM:
	IN 0
	STORE 231

	LOAD 230
	SHL 4
	ADD 231
	STORE 230

	LOAD 210 ; 비밀번호 길이
	ADDI 1
	STORE 210

	JMP CHANGE_WAIT

ONE_MORE_CHECK1:
	LOAD 243
	CMPI 0
	JNZ ONE_MORE_CHECK2

	LOAD 230
	STORE 243
	LOADI 0
	STORE 230
	STORE 210

	JMP CHANGE_WAIT

ONE_MORE_CHECK2:
	CMP 230
	JZ UPDATE_MASK

	LOADI 1
	STORE 244
	JMP ENTER_CHANGE_MODE

UPDATE_MASK:
	LOAD 210

	CMPI 4
	JZ M4_MASK

	CMPI 5
	JZ M5_MASK

	CMPI 6
	JZ M6_MASK

	CMPI 7
	JZ M7_MASK

	CMPI 8
	JZ M8_MASK

	JMP MAIN_WAIT

M4_MASK:
	LOAD 213
	STORE 218
	JMP CHANGE_DONE

M5_MASK:
	LOAD 214
	STORE 218
	JMP CHANGE_DONE

M6_MASK:
	LOAD 215
	STORE 218
	JMP CHANGE_DONE

M7_MASK:
	LOAD 216
	STORE 218
	JMP CHANGE_DONE

M8_MASK:
	LOAD 217
	STORE 218
	JMP CHANGE_DONE

CHANGE_DONE:
	LOAD 230
	STORE 211 ; 정답 비밀번호에 저장

	LOADI 0
	STORE 230

	JMP MAIN_WAIT


READ_NUMBER:
	IN 0
	STORE 231 ; IN 을 통해 받은 값(ACC에 있음)을 231에 저장

	LOAD 230 ; 기존 입력되어 있던 버퍼 
	SHL 4 ; SHIFT LEFT 4
	ADD 231		
	STORE 230 ; 합친 새로운 입력된 비번 저장
	
	LOAD 234 ; UNLOCK_FLAG = 1 이면 비교 스킵
	CMPI 1
	JZ MAIN_WAIT


COMPARE_PASS:
	LOAD 230 ; 지금까지 입력된 값들
	AND 218 ; 최근 (LEN) 자리만 남고 나머지는 0 (((비밀번호 변경 단계에서 연산데이터 수정 필요)))
	STORE 240

	CMP 211 ; 정답 비교, 일치하면 FLAG = 1
	JZ SET_FLAG

	LOAD 240
	CMP 212 ; 마스터키와 비교
	JZ SET_FLAG

	JMP MAIN_WAIT

SET_FLAG:
	LOADI 1
	STORE 234 ; UNLOCK_FLAG = 1
	JMP MAIN_WAIT

BACKSPACE:
	LOAD 230
	SHR 4
	STORE 230
	JMP MAIN_WAIT

COMPLETE_BTN:
	LOAD 234
	CMPI 1
	JZ OPEN_DOOR
	JMP FAIL_PW 

OPEN_DOOR:
	LOADI 8
	OUT 0

	LOADI 0x7FFFFFFF ; 타이머 역할을 위한 숫자(((추후 조절 필요)))
	STORE 241
	
DELAY_LOOP_OPEN:
	LOAD 241
	SUB 242
	STORE 241

	CMPI 0
	JNZ DELAY_LOOP_OPEN

	; TIMER이 0이 되면
	LOADI 0 
	OUT 0
	STORE 234 ; UNLOCK_FLAG = 0
	JMP RESET_INPUT

FAIL_PW:
	LOAD 232 ; 실패 카운터 +1
	ADDI 1
	STORE 232

	CMPI 1
	JZ FAIL_1

	CMPI 2
	JZ FAIL_2

	CMPI 3
	JZ FAIL_3

	JMP FAIL_3

FAIL_1:
	LOADI 4 ; 1회 오답
	OUT 0
	JMP RESET_INPUT

FAIL_2:
	LOADI 2 ; 2회 오답
	OUT 0
	JMP RESET_INPUT

FAIL_3:
	LOADI 1 ; 3회 오답
	OUT 0
	JMP LOCKDOWN

RESET_INPUT:
	LOADI 0
	STORE 230
	STORE 231
	JMP MAIN_WAIT

LOCKDOWN: 
	LOADI 0x7FFFFFFF ; 타이머 역할을 위한 숫자(((추후 조절 필요)))
	STORE 241

DELAY_LOOP_LOCK:
	LOAD 241
	SUB 242
	STORE 241

	CMPI 0
	JZ RESET_FAIL ; LOCKDOWN 해제
	JMP DELAY_LOOP_LOCK

RESET_FAIL:
	LOADI 0
	STORE 232
	OUT 0
	JMP RESET_INPUT